import numpy as np
from sklearn.metrics import roc_curve, auc
import csv
import json
from src.config import disability_config as config
from pathlib import Path

def load_scores_from_results(json_path):
    """
    Loads final_score for each person from behavioral_classification.json
    generated by save_detailed_results().
    
    Paramteres
    ---------
    json_path : str
        Path to behavioral_classification.json.

    Returns
    -------
    dict 
        Mapping person_ids to final_score.
    """
    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    return {
        person_id: person_data['final_score']
        for person_id, person_data in data['persons'].items()
    }

def load_ground_truth_csv(csv_path):
    """
    Loads ground truth labels from CVS.
    
    Parameters
    ---------
    csv_path : str
        Path to ground_truth.csv.

    Returns
    -------
    dict 
        Mapping person_ids to ground truth labels.
    """

    ground_truth = {}

    with open(csv_path, newline="", encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            person_key = next((k for k in row if k.strip().lower() in ("person_id", "person", "id")), None)
            label_key = next((k for k in row if k.strip().lower() in ("label", "ground_truth", "gt", "diagnosis")), None)

            if person_key and label_key:
                try:
                    person_id = f"Person_{row[person_key].strip()}"
                    ground_truth[person_id] = int(row[label_key].strip())
                except (ValueError, TypeError):
                    pass

    return ground_truth
def calibrate_threshold(y_true, y_score):
    """
    Determines the optimal decision threshold by maximizing Youden's J statistic
    on the Receiver Operating Characteristic (ROC) curve.

    The function computes the ROC curve from the ground-truth labels and
    prediction scores, evaluates the Area Under the ROC Curve (AUC), and
    identifies the threshold that maximizes the difference between the
    True Positive Rate (TPR) and the False Positive Rate (FPR). This criterion,
    known as Youden's J statistic, provides a threshold that balances sensitivity
    and specificity.

    Parameters
    ----------
    y_true : array-like of shape (n_samples,)
        Ground-truth binary labels, where 0 denotes the negative class and
        1 denotes the positive class.

    y_score : array-like of shape (n_samples,)
        Continuous prediction scores or similarity values produced by the
        classification model. Higher scores indicate a greater likelihood
        of belonging to the positive class.

    Returns
    -------
    optimal_threshold : float
        Decision threshold corresponding to the maximum value of Youden's
        J statistic.

    roc_auc : float
        Area Under the Receiver Operating Characteristic Curve (ROC AUC),
        representing the model's discriminative performance independently
        of the selected decision threshold.

    Notes
    -----
    Youden's J statistic is defined as:

        J = TPR - FPR

    where TPR is the True Positive Rate (sensitivity) and FPR is the
    False Positive Rate. The threshold maximizing J is commonly adopted
    when equal importance is assigned to sensitivity and specificity.
    """

    fpr, tpr, thresholds = roc_curve(y_true, y_score)

    roc_auc = auc(fpr, tpr)

    optimal_idx = np.argmax(tpr - fpr)
    optimal_threshold = thresholds[optimal_idx]

    return optimal_threshold, roc_auc


def analyze_person(person_name, scores, ground_truth):
    """
    Analyze a specific person at threshold 0.5.

    Parameters
    ----------
    person_name : str
        Person ID to analyze.

    scores : dict
        Mapping person_id to final_score.

    ground_truth : dict
        Mapping person_id to label.

    Returns
    -------
    dict or None
        Analysis result, or None if person not found.
    """

    if person_name not in scores:
        return None
    
    score = scores[person_name]
    gt = ground_truth.get(person_name)
    prediction = 1 if score >= 0.5 else 0

    return {
        "person": person_name,
        "ground_truth": gt,
        "score": score,
        "prediction_at_0_5": prediction,
        "correct_at_0_5": prediction == gt if gt is not None else None,
    }



def main():

    results_path = Path(getattr(config, "RESULTS_PATH", "data/output/disability_results/behavioral_classification.json"))

    if not results_path.exists():
        print(f"Error: Classification results file not found: {results_path}")
        return
    
    scores = load_scores_from_results(results_path)
    print(f"Loaded {len(scores)} scores from {results_path}")

    ground_truth_path = Path(getattr(config, "GROUND_TRUTH_PATH", "data/ground_truth/ground_truth.csv"))

    if not ground_truth_path.exists():
        print(f"Error: Ground truth file not found: {ground_truth_path}")
        return
    
    ground_truth = load_ground_truth_csv(ground_truth_path)
    print(f"Loaded {len(ground_truth)} ground truth labels from {ground_truth_path}")

    common_persons = [p for p in scores if p in ground_truth]
    if not common_persons:
        print(f"Error: No common persons found between scores and ground truth.")
        return
    
    y_true = [ground_truth[p] for p in common_persons]
    y_score = [scores[p] for p in common_persons]

    optimal_threshold, roc_auc = calibrate_threshold(
        y_true,
        y_score
    )

    print("\n=== ROC THRESHOLD CALIBRATION ===")
    print(f"AUC: {roc_auc:.4f}")
    print(f"Optimal Threshold: {optimal_threshold:.4f}")

    print("\n=== CLASSIFICATION USING OPTIMAL THRESHOLD ===")

    for person in common_persons:
        gt = ground_truth[person]
        score = scores[person]
        prediction = 1 if score >= optimal_threshold else 0
        status = "OK" if prediction == gt else "WRONG"
        print(f" Person: {person}, Ground Truth: {gt}, Score: {score:.4f}, Prediction: {prediction}, Status: {status}")

    print("\n=== PERSON ANALYSIS ===")
    for person_name in list(scores.keys())[:2]:
        result = analyze_person(person_name, scores, ground_truth)
        if result: 
            print(f"\n{person_name}:")
            for k, v in result.items():
                print(f" {k}: {v}")


if __name__ == "__main__":
    main()